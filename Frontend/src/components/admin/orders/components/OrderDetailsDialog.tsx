import React from "react";
import {
  Dialog,
  DialogContent,
  DialogHeader,
  DialogTitle,
} from "../../../ui/dialog";
import { Card, CardContent } from "../../../ui/card";
import { Button } from "../../../ui/button";
import { Badge } from "../../../ui/badge";
import { Tabs, TabsContent, TabsList, TabsTrigger } from "../../../ui/tabs";
import {
  Clock,
  Package,
  CheckCircle,
  Store,
  MapPin,
  MessageSquare,
  User,
  Printer,
} from "lucide-react";
import { Order, orderStatusColors } from "../types/orders.types";

interface OrderDetailsDialogProps {
  order: Order | null;
  isOpen: boolean;
  onClose: () => void;
  onMarkAsPickedUp: (orderId: string) => void;
  onMarkAsReady: (orderId: string) => void;
  onPrintOrder?: (order: Order) => void;
}

export const OrderDetailsDialog: React.FC<OrderDetailsDialogProps> = ({
  order,
  isOpen,
  onClose,
  onMarkAsPickedUp,
  onMarkAsReady,
  onPrintOrder,
}) => {
  if (!order) return null;

  // Helper function to map new fields to old field names for print function
  const mapToLegacyOrder = (order: Order) => ({
    id: order.id,
    user: order.customerName,
    vendor: order.vendorName,
    items: order.items,
    quantity: order.quantity,
    amount: order.totalAmount,
    pickup: order.pickupTime,
    status: order.status,
    date: order.orderDate,
    address: order.deliveryAddress,
    paymentMethod: order.paymentMethod,
    notes: order.notes,
    customerEmail: order.customerEmail,
    customerPhone: order.customerPhone,
    vendorEmail: order.vendorEmail,
    vendorPhone: order.vendorPhone,
    itemsList: order.itemsList,
    createdAt: order.createdAt,
    pickedUpAt: order.pickedUpAt,
  });

  const printOrder = (order: Order) => {
    const legacyOrder = mapToLegacyOrder(order);
    const printWindow = window.open("", "_blank");
    if (printWindow) {
      printWindow.document.write(`
        <html>
          <head>
            <title>Order ${legacyOrder.id}</title>
            <style>
              body { font-family: Arial, sans-serif; padding: 20px; }
              .header { text-align: center; margin-bottom: 30px; }
              .section { margin-bottom: 20px; }
              .label { font-weight: bold; color: #666; }
              .value { margin-bottom: 10px; }
            </style>
          </head>
          <body>
            <div class="header">
              <h1>Order Receipt</h1>
              <h2>${legacyOrder.id}</h2>
              <p>Date: ${new Date().toLocaleDateString()}</p>
            </div>
            
            <div class="section">
              <div class="label">Customer:</div>
              <div class="value">${legacyOrder.user}</div>
              
              <div class="label">Vendor:</div>
              <div class="value">${legacyOrder.vendor}</div>
              
              <div class="label">Pickup:</div>
              <div class="value">${legacyOrder.pickup}</div>
              
              <div class="label">Address:</div>
              <div class="value">${legacyOrder.address}</div>
            </div>
            
            <div class="section">
              <div class="label">Items:</div>
              <div class="value">${legacyOrder.items} × ${legacyOrder.quantity}</div>
              
              <div class="label">Amount:</div>
              <div class="value" style="font-size: 24px; font-weight: bold;">${legacyOrder.amount}</div>
            </div>
            
            <div class="section">
              <div class="label">Status:</div>
              <div class="value">${legacyOrder.status}</div>
              
              <div class="label">Payment Method:</div>
              <div class="value">${legacyOrder.paymentMethod}</div>
            </div>
            
            ${
              legacyOrder.notes
                ? `
            <div class="section">
              <div class="label">Notes:</div>
              <div class="value">${legacyOrder.notes}</div>
            </div>`
                : ""
            }
            
            <hr style="margin: 30px 0;" />
            <p style="text-align: center; color: #666;">
              Generated by Order Management System
            </p>
          </body>
        </html>
      `);
      printWindow.document.close();
      printWindow.print();
    }
  };

  return (
    <Dialog open={isOpen} onOpenChange={onClose}>
      <DialogContent className="max-w-2xl max-h-[90vh] overflow-y-auto">
        <DialogHeader>
          <DialogTitle>Order Details</DialogTitle>
        </DialogHeader>

        <div className="mt-4 space-y-6">
          {/* Order Header */}
          <div className="flex items-start justify-between">
            <div>
              <h3 className="text-xl font-bold text-gray-900">
                {order.orderNumber}
              </h3>
              <div className="flex items-center gap-2 mt-1">
                <Badge className={orderStatusColors[order.status]}>
                  {order.status}
                </Badge>
                <span className="text-sm text-gray-500">
                  {order.orderDate} • {order.pickupTime}
                </span>
              </div>
            </div>
            <div className="text-right">
              <p className="text-2xl font-bold text-green-600">
                {order.totalAmount}
              </p>
              <p className="text-sm text-gray-500">{order.paymentMethod}</p>
            </div>
          </div>

          {/* Tabs */}
          <Tabs defaultValue="details" className="w-full">
            <TabsList className="grid grid-cols-3">
              <TabsTrigger value="details">Order Details</TabsTrigger>
              <TabsTrigger value="customer">Customer Info</TabsTrigger>
              <TabsTrigger value="timeline">Timeline</TabsTrigger>
            </TabsList>

            {/* Details Tab */}
            <TabsContent value="details" className="space-y-4">
              {/* Items */}
              <Card>
                <CardContent className="p-4">
                  <h4 className="font-semibold mb-3">Items Ordered</h4>
                  <div className="space-y-3">
                    {(order.itemsList || []).map((item, index) => (
                      <div
                        key={item.id || index}
                        className="flex justify-between items-center py-2 border-b last:border-0"
                      >
                        <div>
                          <p className="font-medium">{item.name}</p>
                          <p className="text-sm text-gray-500">
                            Quantity: {item.quantity}
                          </p>
                        </div>
                        <p className="font-semibold">{item.price}</p>
                      </div>
                    ))}
                    <div className="flex justify-between items-center pt-3 border-t">
                      <p className="font-bold">Total</p>
                      <p className="text-lg font-bold text-green-600">
                        {order.totalAmount}
                      </p>
                    </div>
                  </div>
                </CardContent>
              </Card>

              {/* Vendor & Location */}
              <div className="grid grid-cols-1 md:grid-cols-2 gap-4">
                <Card>
                  <CardContent className="p-4">
                    <h4 className="font-semibold mb-3 flex items-center gap-2">
                      <Store className="h-4 w-4" />
                      Vendor Details
                    </h4>
                    <p className="font-medium">{order.vendorName}</p>
                    <p className="text-sm text-gray-500 mt-1">
                      {order.vendorEmail}
                    </p>
                    <p className="text-sm text-gray-500">{order.vendorPhone}</p>
                  </CardContent>
                </Card>

                <Card>
                  <CardContent className="p-4">
                    <h4 className="font-semibold mb-3 flex items-center gap-2">
                      <MapPin className="h-4 w-4" />
                      Pickup Details
                    </h4>
                    <p className="font-medium">{order.deliveryAddress}</p>
                    <div className="flex items-center gap-2 mt-2 text-sm text-gray-600">
                      <Clock className="h-4 w-4" />
                      {order.pickupTime}
                    </div>
                  </CardContent>
                </Card>
              </div>

              {/* Notes */}
              {order.notes && (
                <Card>
                  <CardContent className="p-4">
                    <h4 className="font-semibold mb-3 flex items-center gap-2">
                      <MessageSquare className="h-4 w-4" />
                      Order Notes
                    </h4>
                    <p className="text-gray-600">{order.notes}</p>
                  </CardContent>
                </Card>
              )}
            </TabsContent>

            {/* Customer Tab */}
            <TabsContent value="customer" className="space-y-4">
              <Card>
                <CardContent className="p-4">
                  <h4 className="font-semibold mb-4">Customer Information</h4>
                  <div className="space-y-4">
                    <div>
                      <p className="text-sm text-gray-500">Name</p>
                      <p className="font-medium">{order.customerName}</p>
                    </div>
                    <div>
                      <p className="text-sm text-gray-500">Contact</p>
                      <p className="font-medium">{order.customerEmail}</p>
                      <p className="text-sm text-gray-500">
                        {order.customerPhone}
                      </p>
                    </div>
                  </div>
                </CardContent>
              </Card>
            </TabsContent>

            {/* Timeline Tab */}
            <TabsContent value="timeline" className="space-y-4">
              <Card>
                <CardContent className="p-4">
                  <h4 className="font-semibold mb-4">Order Timeline</h4>
                  <div className="space-y-4">
                    <div className="flex items-start gap-3">
                      <div className="p-2 bg-blue-100 rounded-full">
                        <Clock className="h-4 w-4 text-blue-600" />
                      </div>
                      <div>
                        <p className="font-medium">Order Placed</p>
                        <p className="text-sm text-gray-500">
                          {order.createdAt}
                        </p>
                      </div>
                    </div>

                    {order.status === "In Progress" && (
                      <div className="flex items-start gap-3">
                        <div className="p-2 bg-purple-100 rounded-full">
                          <Package className="h-4 w-4 text-purple-600" />
                        </div>
                        <div>
                          <p className="font-medium">Order In Progress</p>
                          <p className="text-sm text-gray-500">
                            Being prepared by vendor
                          </p>
                        </div>
                      </div>
                    )}

                    {order.status === "Ready for Pickup" && (
                      <div className="flex items-start gap-3">
                        <div className="p-2 bg-blue-100 rounded-full">
                          <Package className="h-4 w-4 text-blue-600" />
                        </div>
                        <div>
                          <p className="font-medium">Ready for Pickup</p>
                          <p className="text-sm text-gray-500">
                            Order is ready for pickup
                          </p>
                        </div>
                      </div>
                    )}

                    {order.status === "Completed" && order.pickedUpAt && (
                      <div className="flex items-start gap-3">
                        <div className="p-2 bg-green-100 rounded-full">
                          <CheckCircle className="h-4 w-4 text-green-600" />
                        </div>
                        <div>
                          <p className="font-medium">Order Completed</p>
                          <p className="text-sm text-gray-500">
                            Picked up at {order.pickedUpAt}
                          </p>
                        </div>
                      </div>
                    )}
                  </div>
                </CardContent>
              </Card>
            </TabsContent>
          </Tabs>

          {/* Action Buttons */}
          <div className="flex flex-col sm:flex-row gap-3 pt-4">
            <Button
              className="flex-1 hover:bg-gray-100 hover:text-gray-900 text-gray-800"
              onClick={onClose}
            >
              Close
            </Button>
            {order.status === "Ready for Pickup" && (
              <Button
                className="flex-1 bg-green-600 hover:bg-green-700 text-white"
                onClick={() => {
                  onMarkAsPickedUp(order.id);
                }}
              >
                <CheckCircle className="h-4 w-4 mr-2" />
                Mark as Picked Up
              </Button>
            )}
            {order.status === "In Progress" && (
              <Button
                className="flex-1 bg-blue-600 hover:bg-blue-700"
                onClick={() => {
                  onMarkAsReady(order.id);
                }}
              >
                <Package className="h-4 w-4 mr-2" />
                Mark as Ready
              </Button>
            )}
            <Button
              variant="outline"
              className="border-green-600 text-green-600 hover:bg-green-50"
              onClick={() => {
                if (onPrintOrder) {
                  onPrintOrder(order);
                } else {
                  printOrder(order);
                }
              }}
            >
              <Printer className="h-4 w-4 mr-2" />
              Print Receipt
            </Button>
          </div>
        </div>
      </DialogContent>
    </Dialog>
  );
};

export default OrderDetailsDialog;
